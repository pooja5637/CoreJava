package com.revision.core.Collection.List;

import java.util.ArrayList;
import java.util.List;

// how to create an arraylist using the ArrayList() constructor
// add new elements to an ArrayList using the add() method.

public class ArrayListClassExample {

	public static void main(String[] args) {

		//created arraylist object
		List<String> fruits = new ArrayList<>();
		
		fruits.add("apple");
		fruits.add("banana");
		fruits.add("mango");
		fruits.add("kiwi");
		fruits.add("orange");
		
		System.out.println(fruits); // [apple, banana, mango, kiwi, orange]


		
		
		
	}

}
